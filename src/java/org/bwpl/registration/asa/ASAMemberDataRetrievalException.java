package org.bwpl.registration.asa;

public class ASAMemberDataRetrievalException extends RuntimeException {

    public ASAMemberDataRetrievalException() {
        super();
    }

    public ASAMemberDataRetrievalException(String message) {
        super(message);
    }

    public ASAMemberDataRetrievalException(String message, Throwable cause) {
        super(message, cause);
    }

    public ASAMemberDataRetrievalException(Throwable cause) {
        super(cause);
    }
}
